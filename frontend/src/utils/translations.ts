export function translateDynamic(text: string | null | undefined, language: Language): string {
  if (!text) return "";
  if (language === "en") return text;

  const dict = dynamicTranslations[language];
  if (!dict) return text;

  const trimmed = text.trim();
  const lowerTrimmed = trimmed.toLowerCase();
  
  if (dict[lowerTrimmed]) {
    return dict[lowerTrimmed];
  }

  // Handle parentheses like: Ramesh Kumar (Bhendi specialist)
  const parenMatch = trimmed.match(/^([^(]+)\s*\(([^)]+)\)$/);
  if (parenMatch) {
    const mainText = parenMatch[1].trim();
    const parenText = parenMatch[2].trim();
    const translatedMain = translateDynamic(mainText, language);
    const translatedParen = translateDynamic(parenText, language);
    return `${translatedMain} (${translatedParen})`;
  }

  // Split word by word but preserve formatting separators
  const parts = trimmed.split(/(\s+|,|\/|-)/);
  const translatedParts = parts.map(part => {
    const wordLower = part.toLowerCase().trim();
    // Strip trailing periods or commas if any inside the splitter, or match
    const cleanWord = wordLower.replace(/[.,]/g, "");
    if (cleanWord && dict[cleanWord]) {
      return dict[cleanWord];
    }
    if (wordLower && dict[wordLower]) {
      return dict[wordLower];
    }
    return part;
  });

  return translatedParts.join("");
}
